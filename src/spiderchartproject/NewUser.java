    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String uname = jTextField1.getText();
        String name = jTextField2.getText();
        String password = jTextField3.getText();
        int a = jComboBox1.getSelectedIndex();
        String access;

        if (a == 0) {
            access = "admin";
        } else {
            access = "student";
        }
        a= jComboBox2.getSelectedIndex();
        String  gender;
        if (a == 0) {
            gender = "male";
        } else {
            gender = "female";
        }

        if (uname == null || name == null || password == null ){
            JOptionPane.showMessageDialog(null, "No user was added.");

        } else {
            if (uname.isEmpty() || name.isEmpty() || password.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Field(s) cannot be empty");
            } else {
                try {
                    Connection conn = DbConnection.getConnection();
                    String query = "select username from users where username=\'" + uname + "\'";
                    Statement stmt = conn.createStatement();
                    ResultSet rs = stmt.executeQuery(query);
                    if (rs.next() == false) {
                        query = "insert into users (username,name,password,access,gender) values(\'" + uname + "\', \'" + name + "\', \'" + password + "\', \'" + access + "\', \'" + gender + "\')";
                        stmt = conn.createStatement();
                        stmt.execute(query);
                        dispose();
                        JOptionPane.showMessageDialog(null, "User added successfully!");
                        
                    }else{
                        JOptionPane.showMessageDialog(null, "Username already exists.");
                    }
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(NewUser.class.getName()).log(Level.SEVERE, null, ex);
                } catch (SQLException ex) {
                    Logger.getLogger(NewUser.class.getName()).log(Level.SEVERE, null, ex);
                }

            }

        }   
    }//GEN-LAST:event_jButton1ActionPerformed

